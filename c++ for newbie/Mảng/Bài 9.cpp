#include "iostream"
using namespace std;
int main(){
    int a,b;
    int m[100][100];
    for (int i=0;i<a;i++){
        for (int o=0;o<b;o++){
            cin>>m[a][b];
        }
    }
    int sum;
    for (int i=0;i<a;i++){
        for (int o=0;o<b;o++){
            sum+=a[i][o];
        }
    }
    cout<<sum;
}